export interface BeanComponent {
  beanId: string;
  percentage: number;
}

export interface CustomBlend {
  id: string;
  name: 'Custom Blend';
  components: BeanComponent[];
}

export interface CoffeeRecipe {
  id: string;
  name: string;
  beanBlend?: string; // ID of preset or custom blend
  customBlend?: CustomBlend; // Custom bean blend
  roast: string;
  drinkType: string;
  espressoShots?: number;
  milk?: string;
  milkAmount?: string;
  sweetener?: string;
  sweetenerAmount?: string;
  flavorSyrup?: string;
  syrupAmount?: string;
  toppings?: string[]; // Array of topping IDs
  description?: string;
  // Brew parameters
  grindSize?: string;   // e.g. 'extra-fine', 'fine', 'medium-fine', 'medium', 'medium-coarse', 'coarse', 'extra-coarse'
  brewTime?: number;    // seconds
  waterTemp?: number;   // Fahrenheit
}

export interface NutritionalInfo {
  calories: number;
  caffeine: number; // mg
  sugar: number; // g
  protein: number; // g
  fat: number; // g
  // Brew-science derived scores (0-100)
  flavorScore: number;
  bitternessScore: number;
  caffeineExtractScore: number; // brew-efficiency modifier (0-100)
}

